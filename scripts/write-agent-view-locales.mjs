function nest(path, value, target) {
  const parts = path.split(".")
  let o = target
  for (let i = 0; i < parts.length - 1; i++) {
    const p = parts[i]
    o[p] = o[p] ?? {}
    o = o[p]
  }
  o[parts[parts.length - 1]] = value
}

function build(langIdx) {
  const agentView = {}
  for (const [path, en, zh] of rows) {
    nest(path, langIdx === 0 ? en : zh, agentView)
  }
  return { agentView }
}

function main() {
  writeFileSync(join(root, "src/locales/en/agentView.json"), JSON.stringify(build(0), null, 2) + "\n", "utf8")
  writeFileSync(join(root, "src/locales/zh/agentView.json"), JSON.stringify(build(1), null, 2) + "\n", "utf8")
  console.log("Wrote agentView.json (en/zh), keys:", rows.length)
}

